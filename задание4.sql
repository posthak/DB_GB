SELECT color,
SUM(CASE 
	WHEN mark = 'BMW' THEN 1
    ELSE 0
    END) as BMW,
SUM(CASE 
	WHEN mark = 'LADA' THEN 1
    ELSE 0
    END) as LADA
FROM auto
WHERE mark in ('BMW','LADA')
GROUP BY color;

